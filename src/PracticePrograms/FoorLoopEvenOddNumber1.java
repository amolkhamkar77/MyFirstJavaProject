package PracticePrograms;

public class FoorLoopEvenOddNumber1 {

	public static void main(String[] args) {
		
		int count = 0;
		for(int i=2; i<=30; i=i+2) // for even numbers
		{
			
			System.out.println(i);
			count++;
		}
		System.out.println("---------------------");
		System.out.println(count);
	}

}
