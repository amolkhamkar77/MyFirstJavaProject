		int a = 12;
		int b = 5;
		
		System.out.println(a+b); // 12+5=17
		System.out.println(a-b); // 12-5=7
		System.out.println(a*b); // 12x5= 60
		System.out.println(a/b); //12 divide by 5 --Quetint will be the ans 2
		System.out.println(a%b); // Reminder will be the ans i.e 2
		
		
