package finalKeyword;

public class FinalVariable {

	public static void main(String[] args) {
		 final int a = 26;
		 // a=a+23;  it can not be possible here..bcoz final value of a=26 can not be changed
		 // if we dont use final word before variable the value of "a" get added by 23
		 System.out.println(a);
	}

}
